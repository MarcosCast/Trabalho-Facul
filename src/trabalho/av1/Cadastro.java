    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        try {
            Produto p = new Produto();
            p.setApresentacao(txtApresentacao.getText().trim());
            p.setCodigoBarras(txtCodigoBarras.getText().trim());
            p.setComissao(Double.parseDouble(txtComissao.getText().trim()));
            p.setDescontConvenio(Integer.parseInt(txtDesconto.getText().trim()));
            p.setDescricao(txtDescricao.getText().trim());
            p.setEstoqMin(Double.parseDouble(txtEstoqMin.getText().trim()));
            p.setEstoqMax(Double.parseDouble(txtEstoqMax.getText().trim()));
            //verificar se esta cheacdo o RBT de estoq
            if(rbtSim.isSelected())
                p.setEstoqueOnline(1);
            else
                p.setEstoqueOnline(0);
            
            p.setMargemLucro(Double.parseDouble(txtMargLucro.getText().trim()));
            p.setNomeReduzido(txtNomeReduzido.getText().trim());
            
            if(rbtSimProm.isSelected())
                p.setPromocao(true);
            else
                p.setPromocao(false);
            
             if(rbtProduto.isSelected())
                p.setTipoProduto(1);
            else
                p.setTipoProduto(0);
            
             p.setUltimoAumento(txtUltAumento.getText().trim());
             p.setUnidade(ddlUni.getSelectedItem().toString());
             p.setValidadePromocao(txtValidadePromocao.getText().trim());
             p.setValorDesconto(Double.parseDouble(txtDesconto.getText().trim()));
             p.setValoraVista(Double.parseDouble(txtValorVista.getText().trim()));
             if(rbtQnt.isSelected())
                 p.setVendaProduto(1);
             else
                 p.setVendaProduto(0);
             
             p.InserirProduto(p);
             JOptionPane.showMessageDialog(null, "Produto Cadastrado com Sucesso!");
             LimparCampos();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ocorreu o seguinte erro: " + e.getMessage());
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

