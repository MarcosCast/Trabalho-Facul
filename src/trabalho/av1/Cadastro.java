    private void btnPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisaActionPerformed
        // TODO add your handling code here:
        String nomeProduto = txtNomeProduto.getText().trim();
        this.pPesquisado = pPesquisado.PesquisarProduto(nomeProduto);
        if(pPesquisado.getDescricao() == null)
            JOptionPane.showMessageDialog(null, "Produto não encontrado!");
        else
        {
            txtApresentacao.setText(pPesquisado.getApresentacao());
            txtCodigoBarras.setText(pPesquisado.getCodigoBarras());
            txtComissao.setText(pPesquisado.getComissao() + "");
            txtCustoOper.setText(pPesquisado.getCustoOperacional() + "");
            txtDesconto.setText(pPesquisado.getDescontConvenio() + "");
            txtDescricao.setText(pPesquisado.getDescricao());
            txtEstoqMin.setText(pPesquisado.getEstoqMin() + "");
            txtEstoqMax.setText(pPesquisado.getEstoqMax() + "");
            if(pPesquisado.getEstoqueOnline() == 1)
            {
                rbtSim.setSelected(true);
                rbtNao.setSelected(false);
            }
            else{
                rbtSim.setSelected(false);
                rbtNao.setSelected(true);
            }
             
            txtMargLucro.setText(pPesquisado.getMargemLucro() + "");
            txtNomeReduzido.setText(pPesquisado.getNomeReduzido());
            if(pPesquisado.isPromocao() == true)
            {
                rbtSimProm.setSelected(true);
                rbtNaoProm.setSelected(false);
            }
            else{
                rbtSimProm.setSelected(false);
                rbtNaoProm.setSelected(true);
            }
            if(pPesquisado.getTipoProduto() == 1)
            {
                rbtProduto.setSelected(true);
                rbtServico.setSelected(false);
            }
            else{
                rbtProduto.setSelected(false);
                rbtServico.setSelected(true);
            }   
            
            txtUltAumento.setText(pPesquisado.getUltimoAumento());
            ddlUni.setSelectedItem(pPesquisado.getUnidade());
            txtValidadePromocao.setText(pPesquisado.getValidadePromocao());
            txtValorCompra.setText(pPesquisado.getValidadePromocao() + "");
            txtDesconto.setText(pPesquisado.getValidadePromocao() + "");
            txtValorVista.setText(pPesquisado.getValoraVista() + "");
            txtValorPromocao.setText(pPesquisado.getValidadePromocao() + "");
            
             if(pPesquisado.getVendaProduto() == 1)
             {
                 rbtQnt.setSelected(true);
                 rbtVal.setSelected(false);
             }
             else {
                 rbtQnt.setSelected(false);
                 rbtVal.setSelected(true);
             }
        }
    }//GEN-LAST:event_btnPesquisaActionPerformed

